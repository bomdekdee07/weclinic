                                  <p>นับตั้งแต่มีการระบาดของโรค โควิด-19 ในเดือนธันวาคม พ.ศ. 2562 และมีการประกาศโดยองค์การอนามัยโลกว่าเป็นโรคระบาดทั่วโลกในเดือนมีนาคม พ.ศ.2563 จนถึงปัจจุบันมีผู้ติดเชื้อโควิด-19 มากกว่า 16 ล้านคนและมีผู้เสียชีวิตมากกว่าหกแสนคน หลายประเทศในโลกได้ใช้มาตรการเพื่อชะลอการแพร่กระจายของโรค โดยการเว้นระยะห่างทางสังคม และปิดประเทศ ซึ่งก่อให้เกิดความกลัวการแพร่ระบาดต่อการติดเชื้อไวรัส ทำให้เกิดความเกลียดชัง และความรุนแรงเพิ่มมากขึ้นในชาติพันธุ์ </p>
                                  <p>ในประเทศไทยมีผู้ติดเชื้อโควิด-19 มากกว่าสามพันคนและมีผู้เสียชีวิตห้าสิบแปดคน และมีผู้ป่วยที่รักษาหายแล้ว และสามารถกลับบ้านได้มากกว่า 95% อย่างไรก็ตาม ได้มีรายงานถึงชุมชนบางแห่งที่ปฏิเสธผู้ติดเชื้อที่รักษา โควิด-19 หายแล้วกลับไปพำนักที่บ้านของตนเองเริ่มมีจำนวนมากขึ้นในประเทศไทย ผู้ติดเชื้อที่รักษาโควิด-19 หายแล้ว บางรายถูกขอให้ออกจากชุมชน จากเพื่อนบ้านที่กลัวการแพร่ระบาดของเชื้อ ชาวบ้านบางคนต่อต้านโรงพยาบาลให้จัดตั้งหน่วยแยกผู้ป่วยติดเชื้อ โควิด-19ที่รักษาหายแล้ว  หรือบุคคลที่ถูกสอบสวน ไม่ให้มากักตัวในหมู่บ้าน</p>
                                  <p>การตีตราและการเลือกปฏิบัติต่อประชากรที่ได้รับผลกระทบจาก โควิด-19 อาจส่งผลกระทบต่อสภาพจิตใจ นอกจากนี้แล้ว ผู้ที่เข้ารับการรักษาในโรงพยาบาลหรือโดนกักกัน อาจมีความเสี่ยงสูงต่อภาวะวิตกกังวลและภาวะซึมเศร้า ในอดีต ชุมชนที่ได้รับผลกระทบจากการระบาดของโรคซาร์ส (SARS) ได้รับผลกระทบเพียงเล็กน้อยในสภาพแวดล้อมของการทำงาน ทางสังคมและที่บ้าน แต่ก่อให้เกิดความทุกข์ทางอารมณ์ ผลกระทบของการถูกตีตรายังคงอยู่แม้ดูเหมือนว่าจะหายดีแล้ว และนำไปสู่ความเครียดทางด้านจิตใจ ภาวะทางการเงิน การถูกตีตราทางสังคม และความเกลียดกลัวชาวต่างชาติ </p>
                                  <p>ผู้ที่สงสัยว่าตนเองติดเชื้อโควิด-19 อาจไม่กล้าแสดงตนเพื่อไปตรวจ เนื่องจากพวกเขากลัวโดนผลกระทบทางสังคม สิ่งเหล่านี้ล้วนเป็นอุปสรรคต่อการตรวจคัดกรองและการป้องกันโรค การตีตราและการเลือกปฏิบัติที่คล้ายคลึงนี้ได้เกิดขึ้นในช่วงที่มีการแพร่ระบาดของเชื้อเอชไอวี มีงานศึกษาวิจัยในประเทศไทยที่ ระบุถึงตัวขับเคลื่อนหลักสำคัญ 4 ประการที่ทำให้เกิดการตีตราและการเลือกปฏิบัติในสถานบริการสุขภาพ ได้แก่ความกลัวต่อการติดเชื้อ นโยบายด้านสิ่งแวดล้อมและสถานบริการด้านสุขภาพ ความคิดเห็นและทัศนคติต่อผู้ป่วยและการเลือกปฎิบัติ การศึกษาวิจัยครั้งนี้ได้ตั้งสมมติฐานว่า ตัวขับเคลื่อนหลักสำคัญ 4 ประการอาจมีส่วนทำให้เกิดการตีตรา และการเลือกปฏิบัติต่อผู้ติดเชื้อโควิด-19 และมีวัตถุประสงค์เพื่อประเมินกิจกรรมต่างๆที่อาจทำให้สิ่งเหล่านี้ลดลงได้</p>
                                </p>
                                <p class="mt-4">
                                  <b>วัตถุประสงค์หลัก </b><br>
                                  1.	เพื่อศึกษาผลจากการทำกิจกรรมแลกเปลี่ยนเรียนรู้แบบมีส่วนร่วม  การเตรียมชุมชน และการรณรงค์สาธารณะในการลดการตีตราและการเลือกปฏิบัติต่อชุมชนที่ได้รับผลกระทบจากโควิด-19<br>
                                  <ul>
                                    <li>1.1	การเตรียมความพร้อมของชุมชนรวมถึงการให้ความรู้และประเด็นความอ่อนไหวในชุมชน ก่อนผู้ป่วยติดเชื้อโควิด-19 หรือบุคคลเข้าเกณฑ์สอบสวนโรคจะกลับบ้าน</li>
                                    <li>1.2	การรณรงค์เพื่อแก้ไขช่องว่างของความรู้และเสนอมุมมองด้านมนุษยธรรมที่เกี่ยวข้องกับโควิด-19</li>
                                  </ul>

                                  <br><b>วัตถุประสงค์รอง</b><br>
                                  1.   เพื่อสำรวจรูปแบบต่าง ๆ ของการตีตราทางสังคม การตีตราต่อตนเอง การตีตราที่ซับซ้อน และ
                                  ประสบการณ์การเลือกปฏิบัติจากประชาชนผู้ได้รับผลกระทบจากโควิด-19<br>
                                  2.   เพื่อศึกษาปัจจัยที่เกี่ยวข้องกับรูปแบบของการตีตราโรคติดเชื้อโควิด-19 และการเลือกปฏิบัติ<br>
                                  3.   เพื่อรวบรวมวิธีแก้ปัญหา และจัดทำเป็นแนวปฏิบัติเพื่อลดการตีตราและการเลือกปฏิบัติ<br>

                                </p>
                                <p class="mt-4">
                                  <b>เกณฑ์การเข้าร่วมโครงการ</b><br>

                                  1.	บุคลากรทางการแพทย์ทำงานในโรงพยาบาล เอ ที่ดูแลผู้ติดเชื้อโควิด -19 และหรือผู้เข้าเกณฑ์สอบสวนโรค
                        <br>
                                  2.	อายุมากกว่าหรือเท่ากับ 18 ปีขึ้นไป <br>
                                  3.	เต็มใจเข้าร่วมโครงการ<br>
                                  4.	สัญชาติไทย<br>
                                  5.  เซ็นยินยอมเข้าร่วมโครงการ<br>

                                </p>
                                <p class="mt-4">
                                  <b>เกณฑ์การคัดออกจากโครงการ</b><br>
                                  <p>ไม่มี</p>
                                </p>
                                <p class="mt-4">
                                  <b>กระบวนการของการศึกษาวิจัย</b><br>
                                  <p>
                                    <br>1.  แบบสอบถามเรื่องความรู้ ความเข้าใจ ความคิดเห็น/ ทัศนคติ ความตระหนัก รับรู้เรื่องการตีตรา
                                  	      และการเลือกปฏิบัติต่อผู้ติดเชื้อโควิด-19และผู้ถูกสอบสวนโรค ในสถานพยาบาลที่ท่านทำงานอยู่
                                        ใช้เวลาประมาณ 20-30 นาที
                                  <br>2.	หากท่านได้รับการคัดเลือก เราจะขอเชิญท่านเข้าร่วมการกระบวนการแลกเปลี่ยนและเรียนรู้แบบมีส่วนร่วม นอกจากนั้นแล้ว ท่านจะได้รับชม วิดีโอการรณรงค์สาธารณะสั้น ๆ เกี่ยวกับการตีตราและการเลือกปฏิบัติ รวมถึงนโยบายสาธารณะต่างๆเป็นต้น และตอบแบบสอบถามอีกครั้งหลังเสร็จกิจกรรมกระบวนการเรียนรู้และแลกเปลี่ยนแบบมีส่วนร่วม ใช้เวลาประมาณ 3 – 5 ชั่วโมง
                                  <br>3.	หลังจากนั้นทีมผู้วิจัยจะดำเนินการเก็บรวบรวมและวิเคราะห์ข้อมูลที่ได้จากการทำกิจกรรมกลุ่ม จัดทำเป็นคู่มือมาตรฐานหรือจัดทำเป็นแนวทางปฎิบัติ เพื่อลดการตีตราและเลือกปฏิบัติ และนำเสนอแก่ผู้บริหารของท่าน ทีมผู้วิจัยจะขอให้ท่านตอบแบบสอบถามชุดเดิมอีกครั้งหลังจาก 1-3 เดือน (ทุกท่าน)  โดยประมาณหลังจากการตอบแบบสอบถามครั้งแรก ใช้เวลาประมาณ 20-30 นาที

                                  </p>


                                </p>
                                <p class="mt-4">
                                  <b>กระบวนการ</b> แบ่งเป็น 4 ส่วนสำคัญ<br>
                                  <br>1.	การทำความรู้จักรูปแบบและความหมาย กระบวนการแลกเปลี่ยนเรียนรู้เพื่อร่วมกันเรียนรู้และทำความเข้าใจเรื่องการตีตราและการเลือกปฏิบัติ ผ่านสื่อประกอบการเรียนรู้ เช่น รูปภาพวาดจำลองรูปแบบการตีตราและการเลือกปฏิบัติ คลิปวิดีโอ เกม ฯลฯ เพื่อทำความรู้จักกับ ปัจจัยหลัก 4 ปัจจัยที่ทำให้เกิดการตีตราและการเลือกปฏิบัติฯ และได้มาซึ่งรูปแบบของการตีตราและเลือกปฏิบัติที่เกิดขึ้นดังนี้
            <ul>
                                  <li>1.1	ความไม่ตระหนัก เรื่องคำพูด สีหน้า ท่าทาง การกระทำ พฤติกรรมการแสดงออก ที่เป็นการตีตราและการเลือกปฏิบัติ ทั้งโดยตั้งใจหรือไม่ได้ตั้งใจ</li>
                                  <li>1.2	ความกลัวและความกังวล ที่จะได้รับเชื้อจากการดูแล และการอยู่ร่วมกัน ที่นำไปสู่การป้องกันตนเองเกินมาตรฐาน หรือการเลือกปฏิบัติรูปแบบต่าง ๆ ทั้งการแบ่งแยก การกีดกัน การหลีกห่าง และการขับไล่</li>
                                  <li>1.3	ทัศนคติตัดสินเชิงลบ เป็นความคิดเชิงตัดสินแบบเหมารวมที่มองว่า ที่นำไปสู่การตำหนิ และการเลือกปฏิบัติต่อผู้ติดเชื้อโควิด-19 และผู้เข้าเกณฑ์สอบสวนโรคว่าเป็นผู้ที่มีพฤติกรรมไม่เหมาะสม และไม่มีความรับผิดชอบ </li>
                                  <li>1.4	สภาพแวดล้อม เป็นการจัดรูปแบบ สถานที่หรือระบบการดูแลรักษา แนวปฏิบัติ การดูแลข้อมูลความเป็นส่วนตัว การกำหนดกฎเกณฑ์การอยู่ร่วมกันในชุมชน ที่มีลักษณะการแบ่งแยก กีดกัน</li>
            </ul>
                                  <br>2.	การทำความเข้าใจสาเหตุ/ที่มาที่ไป และผลกระทบ กระบวนการเรียนรู้ผ่านการใช้สื่อและชวนผู้เข้าร่วมแสดงความคิดเห็น พูดคุยแลกเปลี่ยน และชวนวิเคราะห์เรื่องพฤติกรรมหรือเนื้อหาที่เห็นผ่านสื่อ ในเรื่องสาเหตุ/ที่มาที่ไป ผลกระทบที่เกิดขึ้นทั้งกับผู้ถูกกระทบและผู้กระทำ รวมถึงการนำรูปแบบการตีตราและเลือกปฏิบัติที่เกิดขึ้น(ทั้งจากการระดมในกลุ่มและจากผลการประเมินครั้งแรก)มาพูดคุยและสรุปร่วมกัน
                                  <br>3.	การแสวงหาแนวทางปรับลด/แก้ไข กระบวนการชวนให้ผู้เข้าร่วมนำรูปแบบการตีตราและเลือกปฏิบัติต่อผู้ติดเชื้อโควิด-19 และผู้ที่อยู่ในเกณฑ์สอบสวนโรคที่ผ่านการค้นหาสาเหตุแล้วมาหาแนวทางแก้ไขปัญหาร่วมกัน ทั้งในระดับบุคคล และการจัดระบบการดูแล แนวปฏิบัติในระบบการดูแลรักษา และการกำหนดแนวทางในการอยู่ร่วมกันในชุมชน
                                  <br>4.	การดำเนินการส่งเสริมการปรับลด/แก้ไขเชิงระบบ ผู้ดำเนินกระบวนการและทีมวิจัยสรุปข้อเสนอแนะเรื่องแนวทางการแก้ไขปัญหาการตีตราและการเลือกปฏิบัติเพื่อนำไปเสนอให้กับ กรรมการชุมชน และเจ้าหน้าที่ผู้ดูแลระบบหรือฝ่ายนิติบุคคลของชุมชนที่พักอาศัย เพื่อการพิจารณาและสนับสนุนการปรับปรุงที่เหมาะสมกับบริบทของชุมชม

                                </p>

                                <p class="mt-4">
                                  <b>จำนวนอาสาสมัครที่เข้าร่วมโครงการ</b><br>
                                  <p>กลุ่มตัวอย่างของกลุ่มนี้รวมเอาบุคลากรทางการแพทย์ ผู้ปฏิบัติงานที่หน่วยให้บริการโควิด-19 ผู้วิจัยจะเลือกอาสาสมัครด้วยวิธีสุ่มตัวอย่างแบบเจาะจงมาเข้าร่วมกิจกรรม ส่วนข้อมูลจากการสัมภาษณ์กระบวนการเรียนรู้แลกเปลี่ยนแบบมีส่วนร่วมจะได้จากอาสาสมัครคัดเลือก จำนวน 15 คน </p>

                                </p>
                                <p class="mt-4">
                                  <b>ระยะเวลาการทำ กระบวนการเรียนรู้และแลกเปลี่ยนแบบมีส่วนร่วม</b><br>
                                  <p>จำนวน 3 – 5 ชั่วโมง (ภายใน 1 วัน)</p>

                                </p>

                                <p class="mt-4">
                                  <b>การวัดผลความเปลี่ยนแปลง</b> มี 2 ส่วน<br>
