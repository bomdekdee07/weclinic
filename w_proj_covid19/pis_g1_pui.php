                        <br>4.	ท่านจะได้รับการอธิบายถึงประโยชน์ที่ท่านอาจจะได้รับจากการวิจัย
                        <br>5.	ท่านจะมีโอกาสได้ซักถามเกี่ยวกับงานวิจัยหรือขั้นตอนที่เกี่ยวข้องกับงานวิจัย
