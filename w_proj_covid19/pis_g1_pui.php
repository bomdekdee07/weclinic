<br>-	ระยะแรกก่อนการเข้าร่วมกิจกรรม (ใช้สำรวจผู้เข้าร่วมการวิจัยทั้งหมด) เพื่อใช้เป็นข้อมูลพื้นฐาน
<br>-	ระยะที่สอง ทันทีหลังสิ้นสุดการเข้าร่วมกิจกรรม (ใช้สำรวจผู้เข้าร่วมการวิจัยที่เข้าร่วมกิจกรรม)
<br>2.	พิจารณาการดำเนินต่อเนื่องจากข้อเสนอเรื่องแนวปฏิบัติเพื่อลดการตีตราและการเลือกปฏิบัติฯ ที่ได้จากการจัดกิจกรรมแลกเปลี่ยนเรียนรู้แบบมีส่วนร่วม
</p>
