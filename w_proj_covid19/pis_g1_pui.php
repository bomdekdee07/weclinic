                    <p class="mt-4">
                      <b>กระบวนการของการศึกษาวิจัย</b><br>
                      ผู้วิจัยจะทำการเก็บข้อมูลจากอาสาสมัครที่เข้าร่วมโครงการ จากกลุ่มตัวอย่าง 4 ได้แก่
                    <br>-	กลุ่มที่หนึ่งบุคคลที่ได้รับผลกระทบจาก โควิด-19และ ผู้เข้าเกณฑ์สอบสวนโรค
                    <br>-	กลุ่มที่สองได้แก่บุคคลที่อาศัยอยู่ในชุมชนเดียวกันกับผู้ที่ได้รับผลกระทบ (รวมถึงแต่ไม่จำกัดเฉพาะ ครอบครัว เพื่อนบ้าน เจ้าของร้าน นิติบุคคล พนักงานรักษาความปลอดภัย เจ้าหน้าที่ทำความสะอาด เป็นต้น) และ ชุมชนท้องถิ่น
                    <br>-	กลุ่มที่สามได้แก่บุคลากรทางการแพทย์
                    <br>-	กลุ่มที่สี่ได้แก่บุคคลที่เข้าร่วมในการสำรวจออนไลน์ (สาธารณะ)


                    </p>
                    <p class="mt-4">
                      <b>กลุ่มที่ได้รับผลกระทบจาก โควิด-19และ ผู้เข้าเกณฑ์สอบสวนโรค</b><br>
                      มีขั้นตอนและกระบวนการดังต่อไปนี้
<br>1.	การตอบแบบสอบถามเพื่อประเมินการรับรู้และประสบการณ์ของท่าน ใช้เวลาประมาณ 20-30 นาที
<br>2.	หากท่านได้รับการคัดเลือก เราจะขอเชิญท่านเข้าร่วมการกระบวนการแลกเปลี่ยนและเรียนรู้แบบมีส่วนร่วม นอกจากนั้นแล้ว ท่านจะได้รับชม วิดีโอการรณรงค์สาธารณะสั้น ๆ เกี่ยวกับการตีตราและการเลือกปฏิบัติ รวมถึงนโยบายสาธารณะต่างๆเป็นต้น และตอบแบบสอบถามอีกครั้งหลังเสร็จกิจกรรมกระบวนการเรียนรู้และแลกเปลี่ยนแบบมีส่วนร่วม ใช้เวลาประมาณ 3 – 5 ชั่วโมง

                    </p>
                    <p class="mt-4">
                      <b>กระบวนการเรียนรู้และแลกเปลี่ยนแบบมีส่วนร่วม</b> แบ่งเป็น 4 ส่วนสำคัญ<br>
                      <br>1.	การทำความรู้จักรูปแบบและความหมาย กระบวนการแลกเปลี่ยนเรียนรู้เพื่อร่วมกันเรียนรู้และทำความเข้าใจเรื่องการตีตราและการเลือกปฏิบัติ ผ่านสื่อประกอบการเรียนรู้ เช่น รูปภาพวาดจำลองรูปแบบการตีตราและการเลือกปฏิบัติ คลิปวิดีโอ เกม ฯลฯ เพื่อทำความรู้จักกับ ปัจจัยหลัก 4 ปัจจัยที่ทำให้เกิดการตีตราและการเลือกปฏิบัติฯ และได้มาซึ่งรูปแบบของการตีตราและเลือกปฏิบัติดังนี้
<ul>
                      <li> 1.1	ความไม่ตระหนัก เรื่องคำพูด สีหน้า ท่าทาง การกระทำ พฤติกรรมการแสดงออก ที่เป็นการตีตราและการเลือกปฏิบัติ ทั้งโดยตั้งใจหรือไม่ได้ตั้งใจ</li>
                      <li> 1.2	ความกลัวและความกังวล ที่จะได้รับเชื้อจากการดูแล และการอยู่ร่วมกัน ที่นำไปสู่การป้องกันตนเองเกินมาตรฐาน หรือการเลือกปฏิบัติรูปแบบต่าง ๆ ทั้งการแบ่งแยก การกีดกัน การหลีกห่าง และการขับไล่</li>
                      <li> 1.3	ทัศนคติตัดสินเชิงลบ เป็นความรู้สึกในด้านลบที่มีต่อตัวเองของผู้ติดเชื้อโควิด-19 และผู้เข้าเกณฑ์สอบสวนโรคด้วยเช่นรู้สึกกล่าวโทษหรือมองว่าเป็นความผิดของตนเอง</li>
                      <li> 1.4	สภาพแวดล้อม เป็นการจัดรูปแบบ สถานที่หรือระบบการดูแลรักษา แนวปฏิบัติ การดูแลข้อมูลความเป็นส่วนตัว การกำหนดกฎเกณฑ์การอยู่ร่วมกันในชุมชน ที่มีลักษณะการแบ่งแยก กีดกัน</li>
</ul>
                      <br>2.	การทำความเข้าใจสาเหตุ/ที่มาที่ไป และผลกระทบ กระบวนการเรียนรู้ผ่านการใช้สื่อและชวนผู้เข้าร่วมแสดงความคิดเห็น พูดคุยแลกเปลี่ยน และชวนวิเคราะห์เรื่องพฤติกรรมหรือเนื้อหาที่เห็นผ่านสื่อ ในเรื่องสาเหตุ/ที่มาที่ไป ผลกระทบที่เกิดขึ้นทั้งกับผู้ถูกกระทบและผู้กระทำ รวมถึงการนำรูปแบบการตีตราและเลือกปฏิบัติที่เกิดขึ้น (ทั้งจากการระดมในกลุ่มและจากผลการประเมินครั้งแรก) มาพูดคุยและสรุปร่วมกัน
                      <br>3.	การแสวงหาแนวทางปรับลด/แก้ไข กระบวนการชวนให้ผู้เข้าร่วมนำรูปแบบการตีตราและเลือกปฏิบัติต่อผู้ติดเชื้อโควิด-19 และผู้เข้าเกณฑ์สอบสวนโรคที่ผ่านการค้นหาสาเหตุแล้วมาหาแนวทางแก้ไขปัญหาร่วมกัน ทั้งในระดับบุคคล และการจัดระบบการดูแล แนวปฏิบัติในระบบการดูแลรักษา และการกำหนดแนวทางในการอยู่ร่วมกันในชุมชน
                      <br>4.	หากพบรูปแบบการตีตราภายใน (การตีตราตนเอง)จะมีการพิจารณาจัดกิจกรรมเพิ่มในส่วนของ การเรียนรู้พื้นฐานเรื่องการตีตราภายใน(การตีตราตนเอง) การสนทนากลุ่มเพื่อแลกเปลี่ยนประสบการณ์การถูกตีตราและเลือกปฏิบัติและแนวทางการแก้ไข/จัดการกับผลกระทบเพิ่มเติม

                    </p>
