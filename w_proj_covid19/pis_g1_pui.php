                        <br>8.	ท่านมีสิทธิ์ในการตัดสินใจว่าจะเข้าร่วมในโครงการวิจัยหรือไม่ก็ได้ โดยปราศจากการใช้อิทธิพลบังคับข่มขู่ หรือการหลอกลวง

