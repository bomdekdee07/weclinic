                      <p>กรณีการจัดกระบวนการในกลุ่มผู้ติดเชื้อโควิด-19 และผู้เข้าเกณฑ์สอบสวนโรคที่เป็นกลุ่มสนทนาแลกเปลี่ยนประสบการณ์ฯ ผู้ดำเนินกระบวนการจะมีการสังเกตผลกระทบทางจิตใจผ่านบทสนทนาและการแสดงออกในระหว่างการสนทนาแลกเปลี่ยน และใช้กระบวนการพูดคุยเยียวยาของกลุ่มเพื่อช่วยเหลือเบื้องต้น รวมทั้งมีการประเมินหลังสิ้นสุดกิจกรรมเรื่องความจำเป็นในการได้รับการส่งต่อเพื่อรับบริการบำบัดเยียวยาจิตใจ</p>
