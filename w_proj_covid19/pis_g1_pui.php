                    <p class="mt-4">
                      <b>ความเสี่ยง และ/หรือ ความไม่สะดวกสบาย</b><br>
                      <p>ความเสี่ยงของท่านต่อการเข้าร่วมให้สัมภาษณ์นี้ คือ บางคำถามอาจทำให้ท่านรู้สึกอึดอัดหรือทำให้ท่านรู้สึกไม่สบายใจ อย่างไรก็ตาม ท่านไม่จำเป็นต้องตอบคำถามใดๆ ที่ท่านไม่อยากตอบ และท่านสามารถที่จะหยุดตอบคำถามเวลาใดก็ได้ นอกจากนี้ ท่านจะได้รับข้อมูลการติดต่อและข้อมูลอ้างอิง หากมีข้อสงสัยประการใดที่ทำให้เกิดปัญหาที่ท่านต้องการแจ้งในเวลานี้หรือในภายหลัง ความเสี่ยงที่มีความเป็นไปได้อีกอย่างสำหรับการเข้าร่วมการศึกษาวิจัยนี้คือการละเมิดข้อมูลส่วนบุคคล </p>
                      <p>ทางโครงการจะพยายามทุกวิถีทางเพื่อปกป้องความเป็นส่วนตัวและความลับของท่าน แต่เป็นไปได้ว่าการมีส่วนร่วมของท่านในการศึกษาวิจัยครั้งนี้อาจเป็นที่รู้จักแก่ผู้อื่น และอาจมีผลร้ายทางสังคม (เช่น การได้รับการปฏิบัติที่ไม่ยุติธรรม หรือ การรังเกียจและตีตรา) เพื่อลดโอกาสของความเสี่ยงเหล่านี้ การสัมภาษณ์ทั้งหมดจะกระทำในห้องที่มีความเป็นส่วนตัว ท่านจะได้รับการขอความยินยอมในการเข้าร่วมกิจกรรมกลุ่มที่เป็นส่วนหนึ่งของโครงการวิจัยก่อนการดำเนินกิจกรรม รวมทั้งการสัมภาษณ์ และในการถอดบทสัมภาษณ์จะไม่มีการระบุชื่อหรือข้อมูลที่แสดงตัวตนของท่านในเอกสารการถอดบทสัมภาษณ์ เพื่อปกป้องความลับของท่าน</p>
                      <p>สำหรับท่านที่ตอบแบบสอบถาม ควรจะบันทึกข้อมูลลงในแบบสอบถามด้วยตนเอง เพื่อลดโอกาสของความเสี่ยงเหล่านี้ ข้อมูลของท่านจะระบุเป็นรหัสส่วนบุคคล</p>
                      <p>นอกจากนี้การสัมภาษณ์กลุ่มบุคคลที่อาศัยอยู่ในชุมชนเดียวกันกับผู้ที่ได้รับผลกระทบ ทีมผู้วิจัยจะประเมินความเสี่ยงก่อนทำกิจกรรมต่างๆ หากมีการประเมินแล้ว พบว่ามีประเด็นเรื่องการอ่อนไหว การถูกตีตราและเลือกปฎิบัติ ทีมผู้วิจัยจะหลีกเลี่ยงการทำกิจกรรมในชุมชนนั้นๆ</p>
                    </p>
