                      <b>จำนวนอาสาสมัครที่เข้าร่วมโครงการ</b><br>
                      <p>ผู้ได้รับผลกระทบจากโรคติดเชื้อโควิด-19  จำนวน 472 คน (126 คนจากผู้ติดเชื้อโควิด-19 และ 346 คน จากผู้เข้าเกณฑ์สอบสวนโรค) ทำแบบสอบถามและการประเมินครั้งแรกและหลังจากติดตาม ส่วนข้อมูลจากการสัมภาษณ์ร่วมเรียนรู้แลกเปลี่ยนประสบการณ์แบบมีส่วนร่วมจะได้จากอาสาสมัครที่คัดเลือกจำนวน 15 คน  </p>

                    </p>
                    <p class="mt-4">
